// ============================================================================
// HELPER FUNCTIONS
// ============================================================================

/**
 * Get story by ID
 */
export function getStoryById(id: string): Story | undefined {
  return STORIES.find((story) => story.id === id)
}

/**
 * Get all stories by level
 */
export function getStoriesByLevel(
  level: 'light' | 'spicy' | 'wild'
): Story[] {
  return STORIES.filter((story) => story.level === level)
}

/**
 * Get total step count for a story
 */
export function getStepCount(storyId: string): number {
  const story = getStoryById(storyId)
  return story ? story.steps.length : 0
}
